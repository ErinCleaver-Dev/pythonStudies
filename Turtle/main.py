import turtle

my_turtle = turtle.Turtle()

def myTurtle(forward, rotateLeft):
    my_turtle.forward(forward)
    my_turtle.left(rotateLeft)
    my_turtle.forward(forward)
    my_turtle.left(rotateLeft)
    my_turtle.forward(forward)
    my_turtle.left(rotateLeft)
    my_turtle.forward(forward)

myTurtle(90, 90)

